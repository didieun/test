package switch_;

import java.util.Scanner;

public class Ex03 {

	public static void main(String[] args) {
		
		Scanner input = new Scanner(System.in);
		String name;
		
		while(true) {
			System.out.println("문자열 입력: ");
			name = input.next();
		
			System.out.println("입력 받은 문자열: " + name);
		}
		
	}

}
